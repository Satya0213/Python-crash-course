million = list(range(1,10000001))

min_value = min(million)                                #minimum value in list
print(f'Minimum value in the list is: {min_value}.\n')

max_value = max(million)                                #maximum value in list
print(f'Maximum value in list is: {max_value}.\n')

sum_of_list = sum(million)                                      #sum of the list
print(f'Sum of numbers from one to one million is: {sum_of_list}.\n')